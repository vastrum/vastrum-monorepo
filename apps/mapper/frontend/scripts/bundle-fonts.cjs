#!/usr/bin/env node
//
// Reads Noto Sans PBF glyph ranges from fonts-master/_output/ and writes
// a single TypeScript module (src/map/font-data.ts) with base64-encoded data.
//
// Only includes specific Unicode ranges to keep the bundle small:
//   - 0-2559:    Latin, IPA, Combining Marks, Greek, Cyrillic, Armenian, Hebrew, Arabic
//   - 7168-9215: Latin Extended Additional, General Punctuation, Symbols
//
// Usage:  node scripts/bundle-fonts.js

const fs = require('fs');
const path = require('path');

const FONTS_OUTPUT = path.resolve(__dirname, '../../fonts-master/_output');
const OUT_FILE = path.resolve(__dirname, '../src/map/font-data.ts');

const FONT_NAMES = ['Noto Sans Regular', 'Noto Sans Bold', 'Noto Sans Italic'];

// Range indices to include (each index i covers codepoints i*256 .. i*256+255)
const RANGE_INDICES = [
    // 0-2559: Basic Latin through Arabic/Syriac/Thaana
    ...Array.from({ length: 10 }, (_, i) => i),
    // 28-35 (7168-9215): Latin Ext Additional, General Punctuation, Symbols
    ...Array.from({ length: 8 }, (_, i) => i + 28),
];

const MIN_FILE_SIZE = 100; // Skip truly empty PBFs (just header, no glyphs)

const entries = {};
let totalRaw = 0;

for (const fontName of FONT_NAMES) {
    const fontDir = path.join(FONTS_OUTPUT, fontName);
    if (!fs.existsSync(fontDir)) {
        console.error(`Missing font directory: ${fontDir}`);
        process.exit(1);
    }

    for (const idx of RANGE_INDICES) {
        const start = idx * 256;
        const end = start + 255;
        const pbfPath = path.join(fontDir, `${start}-${end}.pbf`);

        if (!fs.existsSync(pbfPath)) continue;

        const buf = fs.readFileSync(pbfPath);
        if (buf.length < MIN_FILE_SIZE) continue;

        const key = `${fontName}/${start}-${end}`;
        entries[key] = buf.toString('base64');
        totalRaw += buf.length;
    }
}

const count = Object.keys(entries).length;
console.log(`Bundling ${count} glyph ranges (${(totalRaw / 1024 / 1024).toFixed(2)} MB raw)`);

// Build the TS module
const lines = [
    '// Auto-generated by scripts/bundle-fonts.js  do not edit',
    `// ${count} glyph ranges, ${(totalRaw / 1024 / 1024).toFixed(1)}MB raw`,
    '',
    'const FONT_DATA: Record<string, string> = {',
];

for (const [key, b64] of Object.entries(entries)) {
    lines.push(`  ${JSON.stringify(key)}: ${JSON.stringify(b64)},`);
}

lines.push('};');
lines.push('');
lines.push('export function getGlyphPBF(fontstack: string, range: string): ArrayBuffer | null {');
lines.push('  const b64 = FONT_DATA[`${fontstack}/${range}`];');
lines.push('  if (!b64) return null;');
lines.push('  const bin = atob(b64);');
lines.push('  const buf = new Uint8Array(bin.length);');
lines.push('  for (let i = 0; i < bin.length; i++) buf[i] = bin.charCodeAt(i);');
lines.push('  return buf.buffer;');
lines.push('}');
lines.push('');

fs.writeFileSync(OUT_FILE, lines.join('\n'));
console.log(`Wrote ${OUT_FILE} (${(fs.statSync(OUT_FILE).size / 1024 / 1024).toFixed(2)} MB)`);
